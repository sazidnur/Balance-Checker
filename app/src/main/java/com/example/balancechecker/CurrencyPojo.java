package com.example.balancechecker;

public class CurrencyPojo {
    private double USD;
    private double BDT;
    private double EUR;
    private double GBP;


    // Getter Methods

    public double getUSD() {
        return USD;
    }

    public double getBDT() {
        return BDT;
    }

    public double getEUR() {
        return EUR;
    }

    public double getGBP() {
        return GBP;
    }

    // Setter Methods

    public void setUSD(double USD) {
        this.USD = USD;
    }

    public void setBDT(double BDT) {
        this.BDT = BDT;
    }

    public void setEUR(double EUR) {
        this.EUR = EUR;
    }

    public void setGBP(double GBP) {
        this.GBP = GBP;
    }
}
